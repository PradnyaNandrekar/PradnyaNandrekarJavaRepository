package assignments_PradnyaNandrekar;

public class Assignment103_LogicalOperators 
{

	public static void main(String[] args) 
	{
		if(true && true)
		{
			System.out.println("and operator of true and true");
		}
		if(true || false)
		{
			System.out.println("Or operator of true and false");
		}
		if(!(true && false))
		{
			System.out.println("Not operator of true");
		}

	}

}
