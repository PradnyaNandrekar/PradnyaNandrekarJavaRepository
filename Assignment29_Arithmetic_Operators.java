package assignments_PradnyaNandrekar;

public class Assignment29_Arithmetic_Operators {

	public static void main(String[] args) 
	{
		int a = 10;
		int b = 6;
		int result_add = a + b;
		int result_sub = a - b;
		int result_mul = a * b;
		int result_div = a  / b;
		int result_mod = a % b;
		
		System.out.println("Addition result = "+result_add);		//16
		System.out.println("Substraction result = "+result_sub);	//4
		System.out.println("Multiplication result = "+result_mul);	//60
		System.out.println("Devision result = "+result_div);		//1
		System.out.println("Modulus result = "+result_mod);			//4 - reminder
		
		

	}

}
